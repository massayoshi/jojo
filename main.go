	g.drawNPC(screen, 150, 105)
	g.drawCompanion(screen, float64(playerX-float64(tileSize)), float64(playerY-(float64(tileSize)*1.5)))
	g.drawPlayer(screen, playerX, playerY, g.isPlayerIdle())

