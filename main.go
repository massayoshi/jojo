func (g *Game) Update() error {
	g.count++
	g.handleMovement()

	return nil
}

func (g *Game) handleMovement() {

	if collision := playerObj.Check(playerSpeed, 0); collision != nil {

		// If there was a collision, the "playerObj" Object can't move fully
		// to the right by 2, and Object.Check() would return a *Collision object.
		// A *Collision object contains the Objects and Cells that the calling
		// *resolv.Object ran into when it called Check().

		// To resolve (haha) this collision, we probably want to move the player into
		// contact with that Object. So, we call Collision.ContactWithObject() on the
		// first Object that we came into contact with (which is stored in the Collision).

		// Collision.ContactWithObject() will return a vector.Vector, indicating how much
		// distance to move to come into contact with the specified Object.

		// We could also come into contact with the cell to the right using
		// Collision.ContactWithCell(collision.Cells[0]).
		playerX -= collision.ContactWithObject(collision.Objects[0]).X()
		playerObj.X = playerX
	}

	if ebiten.IsKeyPressed(ebiten.KeyUp) || ebiten.IsKeyPressed(ebiten.KeyW) {
		if playerY > 1 {
			playerY -= playerSpeed
			playerObj.Y -= playerSpeed
		}
		playerSprite = [2]int{0, 96}
	}
	if ebiten.IsKeyPressed(ebiten.KeyDown) || ebiten.IsKeyPressed(ebiten.KeyS) {
		if playerY < (screenHeight - (tileSize * 2)) {
			playerY += playerSpeed
			playerObj.Y += playerSpeed
		}
		playerSprite = [2]int{0, 0}
	}
	if ebiten.IsKeyPressed(ebiten.KeyLeft) || ebiten.IsKeyPressed(ebiten.KeyA) {
		if playerX > 1 {
			playerX -= playerSpeed
			playerObj.X -= playerSpeed
		} else {
			g.changeLevel("left")
		}
		playerSprite = [2]int{0, 32}
	}
	if ebiten.IsKeyPressed(ebiten.KeyRight) || ebiten.IsKeyPressed(ebiten.KeyD) {
		if playerX < (screenWidth - (tileSize * 2)) {
			playerX += playerSpeed
			playerObj.X += playerSpeed
		} else {
			g.changeLevel("right")
		}
		playerSprite = [2]int{0, 64}
	}
	if g.isTurboModeOn() {
		playerSpeed = 10
	} else {
		playerSpeed = 2
	}
}

func (g *Game) changeLevel(direction string) {
	r := rand.Intn(100)
	r2 := rand.Intn(150)
	green := 16
	flower := 2
	fox := 3

	if r > 50 {
		flower = green
	}
	if r > 80 {
		flower = fox
	}

	if r2 < 20 {
		fox = green
	} else if r2 > 20 && r2 < 40 {
		fox = flower
	}

	g.layers = append(g.layers, g.layers[len(g.layers)-1])
	if direction == "left" {
		playerX = (screenWidth - (tileSize * 2))
		playerObj.X = playerX
		g.layers = append(g.layers, []int{green, flower, fox})

	} else {
		playerX = 0
		playerObj.X = playerX
		g.layers = append(g.layers, []int{0, 0, 0, 0, 0, 0, 0, 0, 0, flower, flower, flower, fox})
	}
}

func (g *Game) isKeyJustPressed() bool {
	if inpututil.IsKeyJustPressed(ebiten.KeySpace) {
		return true
	}
	if inpututil.IsMouseButtonJustPressed(ebiten.MouseButtonLeft) {
		return true
	}
	g.touchIDs = inpututil.AppendJustPressedTouchIDs(g.touchIDs)
	if len(g.touchIDs) > 0 {
		return true
	}
	g.gamepadIDs = ebiten.AppendGamepadIDs(g.gamepadIDs[:0])
	for _, g := range g.gamepadIDs {
		if ebiten.IsStandardGamepadLayoutAvailable(g) {
			if inpututil.IsStandardGamepadButtonJustPressed(g, ebiten.StandardGamepadButtonRightBottom) {
				return true
			}
			if inpututil.IsStandardGamepadButtonJustPressed(g, ebiten.StandardGamepadButtonRightRight) {
				return true
			}
		} else {
			// The button 0/1 might not be A/B buttons.
			if inpututil.IsGamepadButtonJustPressed(g, ebiten.GamepadButton0) {
				return true
			}
			if inpututil.IsGamepadButtonJustPressed(g, ebiten.GamepadButton1) {
				return true
			}
		}
	}

	if inpututil.IsKeyJustPressed(ebiten.KeyUp) || inpututil.IsKeyJustPressed(ebiten.KeyW) {
		return true
	}
	// When the "down arrow key" is pressed..
	if inpututil.IsKeyJustPressed(ebiten.KeyDown) || inpututil.IsKeyJustPressed(ebiten.KeyS) {
		return true
	}
	// When the "left arrow key" is pressed..
	if inpututil.IsKeyJustPressed(ebiten.KeyLeft) || inpututil.IsKeyJustPressed(ebiten.KeyA) {
		return true
	}
	// When the "right arrow key" is pressed..
	if inpututil.IsKeyJustPressed(ebiten.KeyRight) || inpututil.IsKeyJustPressed(ebiten.KeyD) {
		return true
	}
	return false
}

