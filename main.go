func init() {
	img, _, err := image.Decode(bytes.NewReader(images.Land_png))
	if err != nil {
		log.Fatal(err)
	}

	tilesImage = ebiten.NewImageFromImage(img)

	img, _, err = image.Decode(bytes.NewReader(images.Player_png))
	if err != nil {
		log.Fatal(err)
	}
	playerImage = ebiten.NewImageFromImage(img)
	playerX = (screenWidth / 2) - (tileSize)
	playerY = (screenHeight / 2) - (tileSize)
	playerSpeed = 2
	playerSprite = [2]int{0, 0}

	img, _, err = image.Decode(bytes.NewReader(images.Companion_png))
	if err != nil {
		log.Fatal(err)
	}
	companionImage = ebiten.NewImageFromImage(img)
}

func init() {
	tt, err := opentype.Parse(fonts.PressStart2P_ttf)
	if err != nil {
		log.Fatal(err)
	}
	const dpi = 72

	arcadeFont, err = opentype.NewFace(tt, &opentype.FaceOptions{
		Size:    fontSize,
		DPI:     dpi,
		Hinting: font.HintingFull,
	})
	if err != nil {
		log.Fatal(err)
	}
}

func (g *Game) Update() error {
	g.count++
	if ebiten.IsKeyPressed(ebiten.KeyUp) || ebiten.IsKeyPressed(ebiten.KeyW) {
		if playerY > 1 {
			playerY = playerY - playerSpeed
		}
		playerSprite = [2]int{0, 96}
	}
	if ebiten.IsKeyPressed(ebiten.KeyDown) || ebiten.IsKeyPressed(ebiten.KeyS) {
		if playerY < (screenHeight - (tileSize * 2)) {
			playerY = playerY + playerSpeed
		}
		playerSprite = [2]int{0, 0}
	}
	if ebiten.IsKeyPressed(ebiten.KeyLeft) || ebiten.IsKeyPressed(ebiten.KeyA) {
		if playerX > 1 {
			playerX = playerX - playerSpeed
		} else {
			g.changeLevel("left")
		}
		playerSprite = [2]int{0, 32}
	}
	if ebiten.IsKeyPressed(ebiten.KeyRight) || ebiten.IsKeyPressed(ebiten.KeyD) {
		if playerX < (screenWidth - (tileSize * 2)) {
			playerX = playerX + playerSpeed
		} else {
			g.changeLevel("right")
		}
		playerSprite = [2]int{0, 64}
	}

	if g.isTurboModeOn() {
		playerSpeed = 10
	} else {
		playerSpeed = 2
	}

	return nil
}

